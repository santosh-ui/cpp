#include<iostream>
int main() {
    int n1 = 15; // Decimal
    int n2 = 017; // Octal
    int n3 = 0x0f; // Hexadecimal
    int n4 = 0b00001111; // Binary
    std::cout << "number 1 is " << n1 << std::endl;
    std::cout << "number 2 is " << n2 << std::endl;
    std::cout << "number 3 is " << n3 << std::endl;
    std::cout << "number 4 is " << n4 << std::endl;
}
